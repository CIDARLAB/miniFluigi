public class Point {
    private int x;
    private int y;

    /**
     * Returns the X coordinate of the point
     * @return
     */
    public int getX() {
        return x;
    }

    /**
     * Sets the X coordinate of the point
     * @param x
     */
    public void setX(int x) {
        this.x = x;
    }

    /**
     * Returns the Y coordinate of the point
     * @return
     */
    public int getY() {
        return y;
    }

    /**
     * Sets the Y coordinate of the point
     * @param y
     */
    public void setY(int y) {
        this.y = y;
    }
}
